    private void listarEmpleados() {
        String filtroBusqueda = txtSearch.getText();
        if (filtroBusqueda.isEmpty()) {
            String query = "SELECT * FROM empleados";

            try {
                connection = conexion.getConnection();
                st = connection.createStatement();
                rs = st.executeQuery(query);
                Object[] empleado = new Object[6];
                contenidoTablaEmpleados = (DefaultTableModel) tblEmpleados.getModel();

                while (rs.next()) {
                    empleado[0] = rs.getInt("idEmp");
                    empleado[1] = rs.getString("nombreEmp");
                    empleado[2] = rs.getString("apellidosEmp");
                    empleado[3] = rs.getString("tipoDocumento");
                    empleado[4] = rs.getString("documento");
                    empleado[5] = rs.getString("correo");
                    contenidoTablaEmpleados.addRow(empleado);
                    tblEmpleados.setModel((TableModel) contenidoTablaEmpleados);
                }

            } catch (SQLException e) {
                System.out.println("Nose pudo cargar informacion de los empleados");
            }
        } else {
            String query = "SELECT * FROM empleados WHERE nombreEmp LIKE '%" + filtroBusqueda + "%' OR apellidosEmp LIKE '%" + filtroBusqueda + "%';";
            try {
                connection = conexion.getConnection();
                st = connection.createStatement();
                rs = st.executeQuery(query);
                Object[] empleado = new Object[6];
                contenidoTablaEmpleados = (DefaultTableModel) tblEmpleados.getModel();

                while (rs.next()) {
                    empleado[0] = rs.getInt("idEmp");
                    empleado[1] = rs.getString("nombreEmp");
                    empleado[2] = rs.getString("apellidosEmp");
                    empleado[3] = rs.getString("tipoDocumento");
                    empleado[4] = rs.getString("documento");
                    empleado[5] = rs.getString("correo");
                    contenidoTablaEmpleados.addRow(empleado);
                    tblEmpleados.setModel((TableModel) contenidoTablaEmpleados);
                }

            } catch (SQLException e) {
                System.out.println("Nose pudo cargar informacion de los empleados");

            }
        }

    }

    private void borrarRegistrosTabla() {
        for (int i = 0; i < tblEmpleados.getRowCount(); i++) {
            contenidoTablaEmpleados.removeRow(i);
            i = i - 1;
        }

    }

